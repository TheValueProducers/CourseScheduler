import re
import json
def tokenize(expr):
    return re.findall(r"[A-Z]{4}\s+\d{3}|AND|OR|\(|\)", expr)

def parse_prereq(expr):
    tokens = tokenize(expr)
    pos = 0

    def parse_expression():
        return parse_or()

    def parse_or():
        nonlocal pos
        left = parse_and()

        conditions = [left]

        while pos < len(tokens) and tokens[pos] == "OR":
            pos += 1
            conditions.append(parse_and())

        if len(conditions) == 1:
            return left

        return {
            "type": "OR",
            "conditions": conditions
        }

    def parse_and():
        nonlocal pos
        left = parse_factor()

        conditions = [left]

        while pos < len(tokens) and tokens[pos] == "AND":
            pos += 1
            conditions.append(parse_factor())

        if len(conditions) == 1:
            return left

        return {
            "type": "AND",
            "conditions": conditions
        }

    def parse_factor():
        nonlocal pos

        token = tokens[pos]

        if token == "(":
            pos += 1
            node = parse_expression()

            if pos < len(tokens) and tokens[pos] == ")":
                pos += 1

            return node

        pos += 1
        return {
            "course": token
        }

    return parse_expression()
