def normalize_course(row):
    subject = str(row["subject"]).strip()

    if pd.isna(row["course_number"]):
        return None

    try:
        course_number = str(int(row["course_number"]))
    except:
        return None

    if subject == "" or subject == "nan":
        return None

    return f"{subject} {course_number}"


def main(

    df,

    degree_requirements,

    completed_courses,

    preferred_courses,

    avoid_courses,

    current_term,

    year,

    optimization="balanced"
) -> None:
    model = cp_model.CpModel()

    distribution_requirements = {
        "Distribution Group I": 3,
        "Distribution Group II": 3,
        "Distribution Group III": 3,
    }

    # -----------------------------
    # Precompute course metadata
    # -----------------------------

    course_to_row = {}
